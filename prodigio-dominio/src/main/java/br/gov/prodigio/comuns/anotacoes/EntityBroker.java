package br.gov.prodigio.comuns.anotacoes;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Retention(RetentionPolicy.RUNTIME)
@Target({ ElementType.TYPE })
public @interface EntityBroker {
	String programa();

	/**
	 * tempo que a entidade pode ficar no cache sem ser acessada
	 */
	long tempoDeVida() default 10 * 180 * 1000;

	long limiteTempoSemAcesso() default 30 * 60 * 1000;

	int quantidadeMaximaPorCache() default 100 * 1000;

	long tempoAtualizacaoCache() default 60 * 1000;

}
